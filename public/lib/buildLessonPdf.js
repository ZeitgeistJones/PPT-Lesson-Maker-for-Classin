/* buildLessonPdf.js
 * Builds a slide-style ESL lesson PDF using jsPDF.
 * Each page is designed like a presentation slide — colored backgrounds,
 * rounded cards, large readable text — optimised for ClassIn's document
 * viewer where the teacher advances pages to pace the lesson.
 *
 * Requires global: jspdf (loaded via CDN <script> tag)
 */

// ── Palette (RGB arrays) ────────────────────────────────────────────
const C = {
  blue:       [59, 130, 246],
  navy:       [30, 58, 95],
  navyLight:  [55, 82, 120],
  coral:      [255, 107, 107],
  yellow:     [255, 217, 61],
  green:      [52, 211, 153],
  lavender:   [238, 240, 255],
  warmYellow: [255, 249, 219],
  peach:      [255, 240, 240],
  mint:       [224, 255, 244],
  white:      [255, 255, 255],
  dark:       [30, 41, 59],
  gray:       [100, 116, 139],
  lightGray:  [241, 245, 249],
  purple:     [139, 92, 246],
};

// Landscape A4 in mm
const W = 297;
const H = 210;
const M = 20; // margin

// Accent-dot colours cycled for lists
const DOTS = [C.coral, C.yellow, C.green, C.blue, C.purple];
// Card backgrounds cycled for vocab / activity lists
const CARDS = [C.lavender, C.warmYellow, C.mint, C.peach];

// ── Helpers ─────────────────────────────────────────────────────────

function fill(doc, rgb) { doc.setFillColor(...rgb); }
function ink(doc, rgb) { doc.setTextColor(...rgb); }
function stroke(doc, rgb) { doc.setDrawColor(...rgb); }

function card(doc, x, y, w, h, rgb, r) {
  fill(doc, rgb);
  doc.roundedRect(x, y, w, h, r ?? 4, r ?? 4, 'F');
}

function bg(doc, rgb) {
  fill(doc, rgb);
  doc.rect(0, 0, W, H, 'F');
}

function deco(doc, cx, cy, r, rgb, opacity) {
  const gs = new doc.GState({ opacity });
  doc.saveGraphicsState();
  doc.setGState(gs);
  fill(doc, rgb);
  doc.circle(cx, cy, r, 'F');
  doc.restoreGraphicsState();
}

function strip(s) {
  return s.replace(/[\u{1F000}-\u{1FFFF}\u{2600}-\u{27BF}\u{FE00}-\u{FE0F}\u{200D}\u{20E3}\u{E0020}-\u{E007F}]/gu, '').trim();
}

function drawText(doc, text, x, y, maxW, opts = {}) {
  const {
    size = 14, bold = false, italic = false, color = C.dark,
    align = 'left', lineH = null,
  } = opts;

  const style = bold && italic ? 'bolditalic' : bold ? 'bold' : italic ? 'italic' : 'normal';
  doc.setFont('helvetica', style);
  doc.setFontSize(size);
  ink(doc, color);

  const cleaned = strip(text);
  const lines = doc.splitTextToSize(cleaned, maxW);
  const lh = lineH ?? size * 0.45;

  const xPos = align === 'center' ? x + maxW / 2 : x;
  doc.text(lines, xPos, y, { align, maxWidth: maxW, lineHeightFactor: 1.35 });

  return y + lines.length * lh;
}

function sectionHeader(doc, label, accentRgb) {
  card(doc, M, M, W - 2 * M, 18, accentRgb ?? C.navy, 5);
  drawText(doc, label, M, M + 12, W - 2 * M, {
    size: 22, bold: true, color: C.white, align: 'center',
  });
}

// ── Page builders ───────────────────────────────────────────────────

function pageTitleSlide(doc, lesson, meta) {
  bg(doc, C.blue);
  deco(doc, -10, -10, 50, C.navy, 0.25);
  deco(doc, W + 10, H + 10, 60, C.navy, 0.20);
  deco(doc, W - 40, 30, 35, C.navy, 0.15);

  drawText(doc, lesson.title, M, 70, W - 2 * M, {
    size: 38, bold: true, color: C.white, align: 'center',
  });

  const sub = `${meta.level}  //  ${meta.duration}-Minute Lesson`;
  drawText(doc, sub, M, 95, W - 2 * M, {
    size: 16, italic: true, color: C.white, align: 'center',
  });

  drawText(doc, 'Generated by ClassIn Lesson Builder', M, H - 16, W - 2 * M, {
    size: 9, color: [180, 210, 255], align: 'center',
  });
}

function pageWarmUp(doc, lesson) {
  bg(doc, C.white);
  sectionHeader(doc, 'Warm Up', C.coral);

  const qY = 50;
  card(doc, M + 8, qY, W - 2 * M - 16, 50, C.lavender, 6);
  drawText(doc, lesson.warmUp.question, M + 16, qY + 20, W - 2 * M - 32, {
    size: 24, bold: true, color: C.navy, align: 'center',
  });

  const aY = 115;
  drawText(doc, 'Sample Answer:', M + 16, aY, W - 2 * M - 32, {
    size: 12, bold: true, color: C.gray,
  });
  card(doc, M + 8, aY + 6, W - 2 * M - 16, 36, C.peach, 5);
  drawText(doc, lesson.warmUp.sampleAnswer, M + 16, aY + 22, W - 2 * M - 32, {
    size: 18, italic: true, bold: true, color: C.coral, align: 'center',
  });

  drawText(doc, 'Let the student answer first before looking at the sample.', M, H - 16, W - 2 * M, {
    size: 9, italic: true, color: C.gray, align: 'center',
  });
}

function pagesVocabulary(doc, lesson) {
  const vocab = lesson.vocabulary.slice(0, 8);

  bg(doc, C.white);
  sectionHeader(doc, 'New Words', C.purple);

  const cols = 2;
  const colW = (W - 2 * M - 12) / cols;
  const perCol = Math.ceil(vocab.length / cols);
  const cardH = Math.min(28, (H - 60) / perCol - 6);

  vocab.forEach((v, i) => {
    const col = i < perCol ? 0 : 1;
    const row = i % perCol;
    const x = M + col * (colW + 12);
    const y = 48 + row * (cardH + 6);
    card(doc, x, y, colW, cardH, CARDS[i % CARDS.length], 4);
    drawText(doc, v.word, x + 8, y + cardH / 2 + 2, colW - 16, {
      size: 20, bold: true, color: C.dark,
    });
  });

  drawText(doc, 'Say each word. Have the student repeat.', M, H - 16, W - 2 * M, {
    size: 9, italic: true, color: C.gray, align: 'center',
  });

  doc.addPage([W, H]);
  bg(doc, C.white);
  sectionHeader(doc, 'New Words \u2014 In Sentences', C.purple);

  const cardH2 = Math.min(34, (H - 56) / perCol - 5);

  vocab.forEach((v, i) => {
    const col = i < perCol ? 0 : 1;
    const row = i % perCol;
    const x = M + col * (colW + 12);
    const y = 46 + row * (cardH2 + 5);
    card(doc, x, y, colW, cardH2, CARDS[i % CARDS.length], 4);
    drawText(doc, v.word, x + 8, y + 11, colW - 16, {
      size: 15, bold: true, color: C.dark,
    });
    drawText(doc, v.sentence, x + 8, y + 22, colW - 16, {
      size: 11, italic: true, color: C.gray,
    });
  });

  drawText(doc, 'Read each example, then ask the student to make their own sentence.', M, H - 16, W - 2 * M, {
    size: 9, italic: true, color: C.gray, align: 'center',
  });
}

function pageSentenceFrames(doc, lesson) {
  bg(doc, C.navy);
  sectionHeader(doc, 'Sentence Frames', C.purple);

  const frames = lesson.sentenceFrames.slice(0, 6);
  const gap = Math.min(24, (H - 70) / frames.length);

  frames.forEach((f, i) => {
    const y = 55 + i * gap;
    fill(doc, DOTS[i % DOTS.length]);
    doc.circle(M + 8, y, 4, 'F');
    drawText(doc, f, M + 20, y + 3, W - 2 * M - 28, {
      size: 20, bold: true, color: C.white,
    });
  });

  drawText(doc, 'Model each frame, then have the student fill in the blank.', M, H - 16, W - 2 * M, {
    size: 9, italic: true, color: [140, 170, 210], align: 'center',
  });
}

// ── Story scene visuals (simple illustrated panels) ─────────────────

function fillTriangle(doc, x1, y1, x2, y2, x3, y3, rgb) {
  fill(doc, rgb);
  doc.lines(
    [[x2 - x1, y2 - y1], [x3 - x2, y3 - y2], [x1 - x3, y1 - y3]],
    x1,
    y1,
    [1, 1],
    'F',
    true
  );
}

function drawPerson(doc, x, y, scale, shirtRgb) {
  const s = scale || 1;
  fill(doc, [255, 220, 185]);
  doc.circle(x, y - 10 * s, 5 * s, 'F');
  fill(doc, shirtRgb || C.coral);
  doc.roundedRect(x - 6 * s, y - 4 * s, 12 * s, 14 * s, 2, 2, 'F');
  stroke(doc, shirtRgb || C.coral);
  doc.setLineWidth(1.2 * s);
  doc.line(x - 6 * s, y, x - 11 * s, y + 6 * s);
  doc.line(x + 6 * s, y, x + 11 * s, y + 6 * s);
  doc.line(x - 3 * s, y + 10 * s, x - 5 * s, y + 18 * s);
  doc.line(x + 3 * s, y + 10 * s, x + 5 * s, y + 18 * s);
}

function drawSceneVisual(doc, x, y, w, h, theme, caption) {
  const t = String(theme || 'nature').toLowerCase();
  card(doc, x, y, w, h, C.lavender, 6);

  // Sky / background band
  const sky = t === 'beach' ? [135, 206, 235]
    : t === 'night' || t === 'city' ? [120, 145, 200]
    : t === 'kitchen' || t === 'home' || t === 'school' ? [210, 230, 255]
    : [180, 220, 255];
  fill(doc, sky);
  doc.roundedRect(x + 3, y + 3, w - 6, h * 0.55, 4, 4, 'F');

  // Ground / floor
  const ground = t === 'beach' ? [244, 220, 150]
    : t === 'kitchen' || t === 'home' || t === 'school' ? [230, 215, 190]
    : t === 'sports' ? [90, 170, 90]
    : [120, 190, 110];
  fill(doc, ground);
  doc.rect(x + 3, y + 3 + h * 0.5, w - 6, h * 0.42, 'F');

  const cx = x + w / 2;
  const midY = y + h * 0.42;

  if (t === 'beach') {
    fill(doc, [255, 210, 70]);
    doc.circle(x + w - 22, y + 18, 10, 'F');
    fill(doc, [70, 150, 220]);
    doc.rect(x + 3, y + 3 + h * 0.42, w - 6, 10, 'F');
    drawPerson(doc, cx - 10, midY + 8, 1.1, C.coral);
    drawPerson(doc, cx + 18, midY + 10, 0.95, C.blue);
  } else if (t === 'city') {
    fill(doc, [80, 100, 140]);
    doc.rect(x + 12, y + 20, 18, 55, 'F');
    doc.rect(x + 36, y + 30, 14, 45, 'F');
    doc.rect(x + 56, y + 16, 22, 59, 'F');
    fill(doc, [255, 230, 120]);
    [[16, 28], [16, 40], [40, 38], [60, 24], [68, 36]].forEach(([dx, dy]) => {
      doc.rect(x + dx, y + dy, 4, 4, 'F');
    });
    drawPerson(doc, cx + 20, midY + 12, 1, C.yellow);
  } else if (t === 'school') {
    fill(doc, [200, 90, 90]);
    doc.rect(x + 20, y + 28, 70, 45, 'F');
    fillTriangle(doc, x + 18, y + 28, x + 55, y + 12, x + 92, y + 28, [120, 70, 50]);
    fill(doc, [180, 220, 255]);
    doc.rect(x + 32, y + 40, 14, 14, 'F');
    doc.rect(x + 58, y + 40, 14, 14, 'F');
    drawPerson(doc, cx + 5, midY + 18, 1, C.purple);
  } else if (t === 'home' || t === 'kitchen') {
    fill(doc, [240, 180, 140]);
    doc.rect(x + 25, y + 35, 65, 42, 'F');
    fillTriangle(doc, x + 22, y + 35, x + 57, y + 14, x + 93, y + 35, [180, 70, 70]);
    fill(doc, [120, 80, 50]);
    doc.rect(x + 50, y + 52, 12, 25, 'F');
    fill(doc, [160, 210, 255]);
    doc.rect(x + 34, y + 44, 12, 12, 'F');
    if (t === 'kitchen') {
      fill(doc, [180, 180, 190]);
      doc.roundedRect(x + 78, y + 55, 22, 14, 2, 2, 'F');
    }
    drawPerson(doc, cx - 5, midY + 16, 1, C.green);
  } else if (t === 'sports') {
    fill(doc, [70, 150, 70]);
    doc.ellipse(cx, midY + 20, 40, 12, 'F');
    fill(doc, [255, 255, 255]);
    doc.setLineWidth(0.6);
    stroke(doc, [255, 255, 255]);
    doc.line(cx - 35, midY + 20, cx + 35, midY + 20);
    drawPerson(doc, cx - 12, midY, 1.1, C.coral);
    fill(doc, [255, 140, 40]);
    doc.circle(cx + 18, midY + 8, 5, 'F');
  } else if (t === 'park' || t === 'nature') {
    fill(doc, [255, 210, 70]);
    doc.circle(x + w - 24, y + 18, 11, 'F');
    fill(doc, [50, 140, 70]);
    doc.circle(x + 28, midY + 2, 16, 'F');
    doc.circle(x + 48, midY - 6, 14, 'F');
    fill(doc, [100, 70, 40]);
    doc.rect(x + 25, midY + 8, 6, 22, 'F');
    doc.rect(x + 45, midY + 4, 5, 26, 'F');
    drawPerson(doc, cx + 15, midY + 10, 1.05, C.blue);
    if (t === 'park') {
      fill(doc, [160, 110, 60]);
      doc.roundedRect(x + 70, midY + 18, 28, 8, 2, 2, 'F');
    }
  } else {
    // default nature-ish
    fill(doc, [255, 210, 70]);
    doc.circle(x + w - 24, y + 18, 11, 'F');
    fill(doc, [60, 160, 90]);
    doc.circle(cx - 20, midY, 18, 'F');
    drawPerson(doc, cx + 10, midY + 8, 1, C.coral);
  }

  // Caption bar
  fill(doc, C.navy);
  doc.roundedRect(x + 8, y + h - 16, w - 16, 11, 2, 2, 'F');
  drawText(doc, caption || theme || 'Scene', x + 8, y + h - 8, w - 16, {
    size: 9, bold: true, color: C.white, align: 'center',
  });
}

function pagesStory(doc, lesson) {
  const story = lesson.story || {};
  const pages = (story.pages || []).slice(0, 2);

  pages.forEach((page, i) => {
    if (i > 0) doc.addPage([W, H]);
    bg(doc, C.white);
    const header = i === 0
      ? `Story: ${story.title || 'Let\'s Read!'}`
      : `Story (cont.): ${page.heading || story.title || 'Part 2'}`;
    sectionHeader(doc, header, C.coral);

    // Left visual panel, right story text
    const panelW = 110;
    const panelH = 130;
    const panelX = M;
    const panelY = 48;
    drawSceneVisual(doc, panelX, panelY, panelW, panelH, page.visualTheme, page.visualCaption);

    const textX = M + panelW + 12;
    const textW = W - textX - M;
    if (i === 0 && page.heading) {
      drawText(doc, page.heading, textX, 56, textW, {
        size: 16, bold: true, color: C.navy,
      });
    } else if (i > 0) {
      drawText(doc, page.heading || 'What happens next…', textX, 56, textW, {
        size: 16, bold: true, color: C.navy,
      });
    }

    card(doc, textX, 66, textW, 112, C.warmYellow, 5);
    drawText(doc, page.text || '', textX + 8, 78, textW - 16, {
      size: 13, color: C.dark, lineH: 6.2,
    });

    drawText(doc, i === 0 ? 'Read together. Point to new words as you go.' : 'Finish the story. Check understanding as you read.', M, H - 16, W - 2 * M, {
      size: 9, italic: true, color: C.gray, align: 'center',
    });
  });

  // Pad to 2 pages if model only returned one
  if (pages.length === 1) {
    doc.addPage([W, H]);
    bg(doc, C.white);
    sectionHeader(doc, 'Story (cont.)', C.coral);
    drawText(doc, 'Continue discussing the story with your student.', M, 80, W - 2 * M, {
      size: 16, color: C.gray, align: 'center',
    });
  }
}

function pageComprehension(doc, lesson) {
  const questions = (lesson.story?.comprehensionQuestions || []).slice(0, 4);
  bg(doc, C.white);
  sectionHeader(doc, 'Reading Comprehension', C.blue);

  drawText(doc, 'Answer in complete sentences. Check the sample after the student answers.', M, 46, W - 2 * M, {
    size: 11, italic: true, color: C.gray, align: 'center',
  });

  const gap = Math.min(36, (H - 70) / Math.max(questions.length, 1));
  questions.forEach((q, i) => {
    const y = 56 + i * gap;
    card(doc, M, y, W - 2 * M, gap - 4, CARDS[i % CARDS.length], 4);
    fill(doc, DOTS[i % DOTS.length]);
    doc.circle(M + 10, y + 10, 4, 'F');
    drawText(doc, `${i + 1}. ${q.question}`, M + 18, y + 12, W - 2 * M - 28, {
      size: 13, bold: true, color: C.dark,
    });
    drawText(doc, `Sample: ${q.sampleAnswer}`, M + 18, y + 22, W - 2 * M - 28, {
      size: 11, italic: true, color: C.gray,
    });
  });
}

